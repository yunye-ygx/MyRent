-- Smart guide location dictionary for local testing
-- Target database: rent (MySQL 8.x)
-- Purpose: provide a minimal place-name -> coordinate dictionary

USE rent;

CREATE TABLE IF NOT EXISTS location_dict (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  name VARCHAR(64) NOT NULL,
  longitude DECIMAL(10,7) NOT NULL,
  latitude DECIMAL(10,7) NOT NULL,
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uk_location_dict_name (name)
) COMMENT='Smart guide local place dictionary';

